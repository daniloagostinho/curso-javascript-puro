// Operadores lógicos

// && (E)
let casado = true;
let empregado = true;

console.log(true && true)
console.log(false && true)
console.log(false && false)
console.log(casado && empregado)

// || (OU)
console.log(false || true)
console.log(true || true)
console.log(false || false)

// ! (NÃO)
console.log(!true)
console.log(!!true)
console.log(!false)

// Operadores de comparação

// > (Maior que)

console.log(10 > 5)
console.log(5 > 10)

// >= (Maior igual)

console.log(10 >= 10)

// < (Menor que)
console.log(2 < 10)
console.log(10 < 2)

// <= (Menor que)
console.log(10 <= 10)

// == (Igual a)

console.log(10 == 10)
console.log(10 == '10')
console.log(10 == 4)

// === (Estritamente igual a)

console.log(10 === '10')
console.log(10 === 10)

// != (Diferente de)

console.log(2 != '2')
console.log(2 != 2)

// !== (Estritamente Diferente de)

console.log(2 !== '10')
console.log(2 != 2)